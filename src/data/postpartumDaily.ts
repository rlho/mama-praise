// totalDaysから適切なメッセージを返す
export function getDailyPostpartumMessage(totalDays: number): {
  title: string
  body: string
  babyInfo: string
} {
  const weekIndex = Math.floor(totalDays / 7)
  const dayIndex = totalDays % 7
  const dataIndex = Math.min(weekIndex, postpartumWeeklyData.length - 1)
  const week = postpartumWeeklyData[dataIndex]

  // タイトル生成
  let title: string
  if (totalDays <= 28) {
    title = `生後${totalDays}日`
  } else {
    const months = Math.floor(totalDays / 30.44)
    const days = Math.round(totalDays - months * 30.44)
    if (months < 12) {
      title = days > 0 ? `生後${months}ヶ月${days}日` : `生後${months}ヶ月`
    } else {
      const years = Math.floor(months / 12)
      const remainMonths = months % 12
      title = remainMonths > 0 ? `${years}歳${remainMonths}ヶ月` : `${years}歳`
    }
  }

  return {
    title,
    body: week.messages[dayIndex],
    babyInfo: week.babyInfo,
  }
}
