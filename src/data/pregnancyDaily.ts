// totalDays(0-280+)からWeekDataとdayIndexを返す
export function getDailyPregnancyMessage(totalDays: number): {
  body: string
  babyInfo: string
  bodyKnowledge: string
} {
  const weekIndex = Math.min(Math.floor(totalDays / 7), weeklyData.length - 1)
  const dayIndex = totalDays % 7
  const week = weeklyData[weekIndex]
  return {
    body: week.messages[dayIndex],
    babyInfo: week.babyInfo,
    bodyKnowledge: week.bodyKnowledge,
  }
}
